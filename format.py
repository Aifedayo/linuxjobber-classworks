# formatting numbers and strings

print(format(5789.467657, "9.3f"))

print(format(5789.467657, "<9.3f"))

print(format(5789.4, ".2f"))

print(format(5789.4, "<.2f"))

print(format(5789.4, ">9.2f"))

print(format("programming is fun", "25s"))

print(format("Programming is fun", "<25s"))

print(format("Programming is fun", ">25s"))